    private void atualizaTabela(CadastroPessoaDao cadastroPDao)
    {
        

                try
                {


                    limparTabela();

                    ArrayList<CadastroPessoas> listaCadastros;
                    listaCadastros = cadastroPDao.consultar(); //consulta todos os registros da tabela Escola

                    //Resgata o modelo da tabela            
                    DefaultTableModel modeloTabela = (DefaultTableModel) Tb01.getModel();

                    for(CadastroPessoas cadastroP : listaCadastros)
                    {
                        //adiciona em cada linha da tabela da tela o conteúdo de cada posição da listaEscolas
                        modeloTabela.addRow(new String[]{Integer.toString(cadastroP.getIdCad()), 
                                                                          cadastroP.getNomeCad(), 
                                                                          cadastroP.getCpf(), 
                                                                          cadastroP.getNomeSexo(),
                                                                          cadastroP.getEmail()});
                    }

                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
                }
     
    }
 

    private void buscarNome(CadastroPessoaDao cadastroPDao)
    {
     
     
            try
            {

                limparTabela();

                ArrayList<CadastroPessoas> listaCadastrosStr;
                listaCadastrosStr = cadastroPDao.consultar(jtbBuscar.getText()); 

                //Resgata o modelo da tabela            
                DefaultTableModel modeloTabela = (DefaultTableModel) Tb01.getModel();

                for(CadastroPessoas cadastroP : listaCadastrosStr)
                {
                    //adiciona em cada linha da tabela da tela o conteúdo de cada posição da listaCadastro
                    modeloTabela.addRow(new String[]{Integer.toString(cadastroP.getIdCad()), 
                                                                      cadastroP.getNomeCad(), 
                                                                      cadastroP.getCpf(), 
                                                                      cadastroP.getNomeSexo(),
                                                                      cadastroP.getEmail()});
                }

            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
            }
      
    }
 
    private void limparTabela()
    {
        //percorre a tabela e exclui todas as linhas
        while(Tb01.getRowCount() > 0) 
        {
            DefaultTableModel dm = (DefaultTableModel) Tb01.getModel();
            dm.getDataVector().removeAllElements();
        }
    }

    private void atualizaSexualidade(SexualidadeDao sexualidadeDao)
    {
        

                try
                {
                    limparCbxSexo();
                    ArrayList<Sexualidade> listarSexualidade;
                    listarSexualidade = sexualidadeDao.consultar(); //consulta todos os registros da tabela Escola

                    for(Sexualidade sexualidade : listarSexualidade)
                    {
                        //adiciona em cada linha da tabela da tela o conteúdo de cada posição da listaEscolas
                        jCBSexo.addItem(sexualidade.getNomeSexo());
                    }
                    jCBSexo.setSelectedIndex(-1);
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
                }
           
        
    }
