    private void Tb01MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tb01MouseClicked
        // TODO add your handling code here:
        if((Tb01.getSelectedRow() != -1) && (vInsUpdate == 0) && (evt.getClickCount() == 2)){
           jId.setText(Tb01.getValueAt(Tb01.getSelectedRow(),0).toString());
           jNome.setText(Tb01.getValueAt(Tb01.getSelectedRow(),1).toString());
           jCpf.setText(Tb01.getValueAt(Tb01.getSelectedRow(),2).toString());
           jCBSexo.setSelectedItem(Tb01.getValueAt(Tb01.getSelectedRow(),3).toString());
           jEmail.setText(Tb01.getValueAt(Tb01.getSelectedRow(), 4).toString());
           vInsUpdate = 1;
           Tb01.setEnabled(false);
           jBCadastrar.setText("Alterar");
           jId.setEnabled(false);
        }   
    }//GEN-LAST:event_Tb01MouseClicked

    private void jCepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jCepKeyPressed
        // TODO add your handling code here:
        if(evt.getExtendedKeyCode() == evt.VK_ENTER){
            ViaCEP viaCep = new ViaCEP();

            try {
                viaCep.buscar(jCep.getText());
                txtBairro.setText(viaCep.getBairro());
                txtRua.setText(viaCep.getLogradouro());
                txtUf.setText(viaCep.getUf());
                txtCidade.setText(viaCep.getLocalidade());

            } catch (ViaCEPException ex) {
                JOptionPane.showMessageDialog(null, 
                        "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jCepKeyPressed

